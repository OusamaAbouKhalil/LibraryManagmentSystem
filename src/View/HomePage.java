    public void showPieChart() {

        //create dataset
        DefaultPieDataset barDataset = new DefaultPieDataset();
        barDataset.setValue("Atomic Habits", new Double(8));
        barDataset.setValue("Beloved", new Double(30));
        barDataset.setValue("The Fault in Our Stars", new Double(40));
        barDataset.setValue("The Self-Taught Programmer", new Double(12));

        //create chart
        JFreeChart piechart = ChartFactory.createPieChart("Books", barDataset, false, true, false);//explain

        PiePlot piePlot = (PiePlot) piechart.getPlot();

        //changing pie chart blocks colors
        piePlot.setSectionPaint("Atomic Habits", new Color(255, 255, 102));
        piePlot.setSectionPaint("Beloved", new Color(102, 255, 102));
        piePlot.setSectionPaint("The Fault in Our Stars", new Color(255, 102, 153));
        piePlot.setSectionPaint("The Self-Taught Programmer", new Color(0, 204, 204));

        piePlot.setBackgroundPaint(Color.white);

        //create chartPanel to display chart(graph)
        ChartPanel barChartPanel = new ChartPanel(piechart);
        panelChart.removeAll();
        panelChart.add(barChartPanel, BorderLayout.CENTER);
        panelChart.validate();
        txtNumberOfBooks.setText(getBooksCount()+"");
    }
